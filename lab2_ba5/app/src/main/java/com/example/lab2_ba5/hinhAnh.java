package com.example.lab2_ba5;

public class hinhAnh {
    private String ten;
    private int hinh;

    public hinhAnh(String ten,int Hinh) {
        this.ten = ten;
        this.hinh = Hinh;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getHinh() {
        return hinh;
    }

    public void setHinh(int hinh) {
        this.hinh = hinh;
    }
}
