// less than < 
let a = 10; 
let b = 2;
let result = 2<10;
document.write(result);// trả về true 
// Greater than >
document.write(a>b);//true
// less than or equal to <= 
document.write(a<=b);//false
// Greater than or equal to >= 
document.write(a>=b);// true
// equal to ==
document.write(a==b);//false
// not equal to !=
document.write(a!=b); //true
// Equal value and same type 
document.write(a===10);// bằng nhau tuyệt đối kiểu số true
document.write(a==="10");// false 
// not equal value and not same type 
document.write(10!==a);// false
document.write(a!=="10");//true






